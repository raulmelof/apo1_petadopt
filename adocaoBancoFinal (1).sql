CREATE DEFINER=`root`@`localhost` PROCEDURE `verificaProntuario`(IN vidAnimal INT(11))
BEGIN
	DECLARE hygiene VARCHAR(20);
    DECLARE doencasCount INT;
    DECLARE vacinasCount INT;

    -- Obter as condições do prontuário do animal
    SELECT higieneAnimal INTO hygiene 
    FROM tb_prontuario 
    WHERE idAnimal = vidAnimal;

    -- Verificar se o animal possui doenças
    SELECT COUNT(*) INTO doencasCount 
    FROM tb_doencas 
    WHERE codProntuario = (SELECT codProntuario FROM tb_prontuario WHERE idAnimal = vidAnimal);
    
    -- Verificar se o animal está vacinado
    SELECT COUNT(*) INTO vacinasCount 
    FROM tb_vacinas 
    WHERE codProntuario = (SELECT codProntuario FROM tb_prontuario WHERE idAnimal = vidAnimal);

    -- Verificar se o animal está pronto para ser disponibilizado
    IF hygiene = 'SIM' AND doencasCount = 0 AND vacinasCount > 0 THEN
        SELECT 'Animal pronto para ser disponibilizado.' AS mensagem;
    ELSE
        SIGNAL SQLSTATE '45000' SET MESSAGE_TEXT = 'O animal não atende aos requisitos para ser disponibilizado.';
    END IF;
END ;;
